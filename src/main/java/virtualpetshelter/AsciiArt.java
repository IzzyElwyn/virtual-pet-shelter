package virtualpetshelter;

public class AsciiArt {

	public static String playWithChurch() {
		System.out.println("      ^  ^         ");
		System.out.println("    ( *  * )       ");
		System.out.println("     /    \\  /TT\\");
		System.out.println("    / |  | \\//  U ");
		System.out.println("   (  u  u  )      ");
		return "You play with Church";
	}
	
	public static String playWithStripe() {
		
		System.out.println("   <TTTT\\~/TTTT>   ");
		System.out.println("      \\* w */      ");
		System.out.println("___w__[_____]_w_____");
		return "You play with Stripe";
	}
	
	public static String playWithBen() {
		
		System.out.println("           )  ");
		System.out.println("   @_@    (   ");
		System.out.println("  (. .)----\\ ");
		System.out.println("   >x< ___  ) ");
		System.out.println("    <-   < -  ");
		return "You play with Ben";
	}
	
	public static String playWithAudreyII() {
		System.out.println("             ______      ");
		System.out.println("            / ~~~~~~\\   ");
		System.out.println("          &/\\ /vvvvv )  ");
		System.out.println("         &/  /\\wwww/    ");
		System.out.println("         <| |~           ");
		System.out.println("          ~\\ \\>~       ");
		System.out.println("          |TTTTT|        ");
		System.out.println("           \\___/        ");
		return "You play with Audrey II";
	}
}
